#!/usr/bin/env node

/**
 * generate-content-config.mjs
 *
 * Reads site-config.json from the content repo (or a fallback path) and
 * generates src/generated/content-config.ts — a TypeScript file whose
 * export is a pure JS literal (no fs calls at runtime), safe to import
 * in both SSR and client bundles.
 *
 * Lookup priority:
 *   1. src/data/site-config.json   — after sync-content.js symlinks content/data/
 *   2. content/data/site-config.json — when cloned directly (CI)
 *
 * If no file is found the committed fallback (empty object) is left intact,
 * and all configs use their code-side defaults.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = path.resolve(__dirname, "..");
const OUTPUT_FILE = path.join(PROJECT_ROOT, "src/generated/content-config.ts");

// Search paths in priority order.
const SEARCH_PATHS = [
	path.join(PROJECT_ROOT, "src/data/site-config.json"),
	path.join(PROJECT_ROOT, "content/data/site-config.json"),
];

// ---------------------------------------------------------------------------
// Walk a value and convert { preset: "Name" } objects to plain strings.
// This keeps the generated TS free of LinkPreset enum imports.
// ---------------------------------------------------------------------------
function convertPresets(value) {
	if (Array.isArray(value)) {
		return value.map(convertPresets);
	}
	if (value !== null && typeof value === "object") {
		// { "preset": "Home" } → "Home"
		if ("preset" in value && Object.keys(value).length === 1) {
			return value.preset;
		}
		const obj = {};
		for (const [k, v] of Object.entries(value)) {
			obj[k] = convertPresets(v);
		}
		return obj;
	}
	return value;
}

// ---------------------------------------------------------------------------
// Generate the TypeScript source
// ---------------------------------------------------------------------------
function generateSource(json, sourcePath) {
	const processed = convertPresets(json);
	const body = JSON.stringify(processed, null, 2);
	const sourceComment = sourcePath
		? `// Source: ${path.relative(PROJECT_ROOT, sourcePath)}`
		: "// No content repo config found — using code defaults.";

	return `// -*- mode: typescript -*-
// AUTO-GENERATED — DO NOT EDIT DIRECTLY
// Generated by scripts/generate-content-config.mjs
${sourceComment}
// Edit data/site-config.json in the content repo instead.

import type { ContentConfig } from "../types/content-config";

export const contentConfig: ContentConfig = ${body};
`;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
function main() {
	// Ensure output directory exists.
	const outDir = path.dirname(OUTPUT_FILE);
	if (!fs.existsSync(outDir)) {
		fs.mkdirSync(outDir, { recursive: true });
	}

	// Find the first existing config file.
	let sourcePath = null;
	for (const p of SEARCH_PATHS) {
		if (fs.existsSync(p)) {
			sourcePath = p;
			break;
		}
	}

	if (!sourcePath) {
		console.log(
			"[generate-content-config] No site-config.json found. Using committed fallback (empty config).",
		);
		return;
	}

	// Read and parse the JSON.
	let raw;
	try {
		const content = fs.readFileSync(sourcePath, "utf-8");
		raw = JSON.parse(content);
	} catch (err) {
		console.error(
			`[generate-content-config] Failed to parse ${sourcePath}:`,
			err.message,
		);
		// Leave the committed fallback intact rather than writing a partial file.
		return;
	}

	// Generate and write.
	const source = generateSource(raw, sourcePath);
	fs.writeFileSync(OUTPUT_FILE, source, "utf-8");
	console.log(
		`[generate-content-config] Wrote ${OUTPUT_FILE} from ${path.relative(PROJECT_ROOT, sourcePath)}`,
	);
}

main();
