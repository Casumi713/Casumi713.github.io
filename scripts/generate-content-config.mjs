#!/usr/bin/env node

/**
 * generate-content-config.mjs
 *
 * Generates src/generated/content-config.ts from content-repo config files.
 * The output is a pure JS literal (no fs calls at runtime), safe to import
 * in both SSR and client bundles.
 *
 * Config file lookup (in priority order):
 *   1. Individual: data/config/<section>.json  (e.g. data/config/navBar.json)
 *   2. Combined:   data/site-config.json       (backward compat, overrides #1)
 *
 * Each path is checked under both:
 *   - src/data/   (after sync-content.js symlinks content/data/)
 *   - content/data/  (when cloned directly, e.g. in CI)
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = path.resolve(__dirname, "..");
const OUTPUT_FILE = path.join(PROJECT_ROOT, "src/generated/content-config.ts");

// Data roots to check (local + CI paths)
const DATA_ROOTS = [
	path.join(PROJECT_ROOT, "src/data"),
	path.join(PROJECT_ROOT, "content/data"),
];

// ---------------------------------------------------------------------------
// Convert { preset: "Name" } objects to plain strings
// ---------------------------------------------------------------------------
function convertPresets(value) {
	if (Array.isArray(value)) {
		return value.map(convertPresets);
	}
	if (value !== null && typeof value === "object") {
		if ("preset" in value && Object.keys(value).length === 1) {
			return value.preset;
		}
		const obj = {};
		for (const [k, v] of Object.entries(value)) {
			obj[k] = convertPresets(v);
		}
		return obj;
	}
	return value;
}

// ---------------------------------------------------------------------------
// Read and parse a JSON file, return null on failure
// ---------------------------------------------------------------------------
function readJson(filePath) {
	try {
		const content = fs.readFileSync(filePath, "utf-8");
		return JSON.parse(content);
	} catch (err) {
		console.error(`[generate-content-config] Failed to parse ${filePath}:`, err.message);
		return null;
	}
}

// ---------------------------------------------------------------------------
// Generate the TypeScript source
// ---------------------------------------------------------------------------
function generateSource(merged, sources) {
	const processed = convertPresets(merged);
	const body = JSON.stringify(processed, null, 2);

	const sourceList = sources.length > 0
		? sources.map((s) => `// Source: ${path.relative(PROJECT_ROOT, s)}`).join("\n")
		: "// No content repo config found — using code defaults.";

	return `// -*- mode: typescript -*-
// AUTO-GENERATED — DO NOT EDIT DIRECTLY
// Generated by scripts/generate-content-config.mjs
${sourceList}
// Edit files in data/config/ or data/site-config.json in the content repo.

import type { ContentConfig } from "../types/content-config";

export const contentConfig: ContentConfig = ${body};
`;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
function main() {
	const outDir = path.dirname(OUTPUT_FILE);
	if (!fs.existsSync(outDir)) {
		fs.mkdirSync(outDir, { recursive: true });
	}

	// 1. Load individual files from data/config/<section>.json
	const merged = {};
	const sources = [];

	for (const root of DATA_ROOTS) {
		const configDir = path.join(root, "config");
		if (!fs.existsSync(configDir)) continue;

		const entries = fs.readdirSync(configDir, { withFileTypes: true });
		for (const entry of entries) {
			if (!entry.isFile() || !entry.name.endsWith(".json")) continue;

			const section = entry.name.slice(0, -5); // strip ".json"
			const filePath = path.join(configDir, entry.name);

			// Avoid duplicate sources — if we already read this section from a higher-priority root, skip
			if (sources.some((s) => s.endsWith(`/config/${entry.name}`))) continue;

			const data = readJson(filePath);
			if (data !== null) {
				// If multiple files in the same dir define the same section, last wins.
				// This also means a section file found in src/data/ replaces one from content/data/
				merged[section] = data;
				sources.push(filePath);
				console.log(`  · ${section} ← ${path.relative(PROJECT_ROOT, filePath)}`);
			}
		}

		// Only process the first root that has a config/ dir (src/data/ takes priority over content/data/)
		if (fs.existsSync(configDir) && fs.readdirSync(configDir).length > 0) {
			break;
		}
	}

	// 2. Load combined file (data/site-config.json) — overrides individual files
	for (const root of DATA_ROOTS) {
		const combinedPath = path.join(root, "site-config.json");
		if (!fs.existsSync(combinedPath)) continue;

		const combined = readJson(combinedPath);
		if (combined !== null && typeof combined === "object") {
			for (const [section, data] of Object.entries(combined)) {
				merged[section] = data;
			}
			sources.push(combinedPath);
			console.log(`  · (combined) ${path.relative(PROJECT_ROOT, combinedPath)}`);
		}
		break; // Only first root
	}

	// 3. Write output
	if (sources.length === 0) {
		console.log("[generate-content-config] No config files found. Using committed fallback (empty config).");
		return;
	}

	const source = generateSource(merged, sources);
	fs.writeFileSync(OUTPUT_FILE, source, "utf-8");
	console.log(`[generate-content-config] ✓ Wrote ${path.relative(PROJECT_ROOT, OUTPUT_FILE)}`);
}

main();
